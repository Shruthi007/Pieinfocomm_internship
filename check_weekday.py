#Weekday

n=int(input("Enter a number:"))
if n==0:
    print("Sunday")
elif n==1:
    print("Monday")
elif n==2:
    print("Tuesday")
elif n==3:
    print("wednesday")
elif n==4:
    print("Thursday")
elif n==5:
    print("friday")
elif n==6:
    print("Saturday")
else:
    print("Not a weekday")
    
